/** Build a list of N photo paths under /public/properties/<folder>/photo_NNN.jpg. */
function photos(folder: string, count: number): string[] {
  return Array.from({ length: count }, (_, i) =>
    `/properties/${folder}/photo_${String(i + 1).padStart(3, "0")}.jpg`
  );
}

